void visualise(char x[10][10]){
    cout << "\033[1;37m"<<endl;
    cout << "   A B C D E F G H I J";

    for(int i = 0; i<10; i++){
        if(i+1 <10){
            cout << "\033[0;37m";
            cout<<endl<<"0"<<i+1<<" ";
            cout << "\033[1;34m";
        }
        else{
            cout << "\033[0;37m";
            cout<<endl<<i+1<<" ";
            cout << "\033[1;34m";
        }
         for(int j = 0; j<10; j++){
            if(x[i][j] != '~'){
                cout << "\033[1;31m";
            }
             cout<<x[i][j]<<" ";
             cout << "\033[1;34m";
        }
    }
    cout << "\033[0;37m";
};

